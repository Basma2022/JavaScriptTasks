var par= document.getElementById("PAR");

function ChangeFont(f)
{
  par.style.fontFamily = f;
}

function ChangeAlign(align)
{
    par.style.textAlign = align;
}

function ChangeHeight(h)
{
    par.style.lineHeight = h;
}

function ChangeLSpace(ls)
{
    par.style.letterSpacing = ls;
}

function ChangeIndent(i)
{
    par.style.textIndent = i; 
}

function ChangeTransform(t)
{
    par.style.textTransform = t; 
}

function ChangeDecorate(d)
{
    par.style.textDecoration = d; 
}

function ChangeBorder(b)
{
    par.style.border = b; 
}

function ChangeBorderColor(c)
{
    par.style.borderColor = c;
}